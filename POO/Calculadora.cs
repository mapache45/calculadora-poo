namespace POO
{
    public class Calculadora
    {
        public double Sumar(double x, double y)
        {
            return x + y;
        }

        public double Restar(double x, double y)
        {
            return x - y;
        }

        public double Multiplicar(double x, double y)
        {
            return x * y;
        }

        public double Dividir(double x, double y)
        {
            return x / y;
        }
    }
}
